//How to push any elements to an array at the last of the elements

var pushPop = [14, 15, 16, 17, 18]

pushPop.push(19);
console.log(pushPop);


//how to pop any elements to an array at the last of the elements
var pushPop = [ 14, 15, 16, 17, 18]

pushPop.pop();

console.log(pushPop);


// Here store to any pop value to a variable
var popValue = pushPop.pop();
console.log(popValue);

//How to push any elements to beginning of array
var pushPop = [ 14, 15, 16, 17, 18]
pushPop.unshift(100);

console.log(pushPop);


//How to pop any elements to beginning of array
var pushPop = [ 14, 15, 16, 17, 18]
pushPop.shift();

console.log(pushPop);