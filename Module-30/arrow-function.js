// Normal Function 
function addNumberWithNormalFunction(a, b) {
    return a + b;
}
const a = 10;
const b = 10;

console.log(addNumberWithNormalFunction(a, b));


// Arrow function 
const arrowFunction = (a, b) => a + b;
console.log(arrowFunction(a, b));


const arrowFunction2 = (a, b) => a * b;
console.log(arrowFunction2(a, b));
